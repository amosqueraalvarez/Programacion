numero = float(input('Ingrese un numero: '))
numero2 = float(input('Ingrese otro numero: '))
while numero >= numero2:
    resta = (numero - numero2)
    print('Los numeros se restan' + str(resta))
    break
else:
    numero < numero2
    suma = (numero + numero2)
    print('Los numeros se suman' + str(suma))
